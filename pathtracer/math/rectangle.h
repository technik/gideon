#pragma once

namespace math {

	template <typename N_>
	struct Rectangle
	{
		Rectangle() = default;
		constexpr Rectangle(N_ _x0, N_ _y0, N_ _x1, N_ _y1)
			:x0(_x0), y0(_y0), x1(_x1), y1(_y1)
		{}
		N_ x0, y0, x1, y1;
		constexpr auto area() const { return (x1-x0)*(y1-y0); }
	};

}